package Qno3;

import java.util.Scanner;

public class Qno3 {
public static void main(String[] args) {
	
	Scanner input = new Scanner(System.in);
	
	System.out.print("Enter your integer:");
	
	int num = input.nextInt();
	
	
		
		for (int j = 0; j < num; j++) {

			for (int i = 0; i <= j; i++) {

				System.out.print("*");

			}

			System.out.println("");
		}


	}



	}

